import java.math.*;

public class BigIntegerDemo {

public static void main(String[] args) {
		BigInteger bi1, bi2, bi3;
		bi1 = new BigInteger("123");
		bi2 = new BigInteger("50");
		
		bi3 = b1.add(bi2);
		String str = "Result of addition is " + bi3;
		
		System.out.println(str);
	}
}
